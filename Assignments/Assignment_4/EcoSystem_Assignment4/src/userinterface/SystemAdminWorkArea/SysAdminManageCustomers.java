    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(validateThis())
        {
            Employee e = ecosystem.getEmployeeDirectory().createEmployee(txtName.getText(), txtAddress.getText(), txtPhone.getText());
            
            UserAccount ua = ecosystem.getUserAccountDirectory().createUserAccount(txtUsername.getText(), txtPassword.getText(), e, new CustomerRole());
            if(ua != null)
            {
                Customer c = ecosystem.getCustomerDirectory().createCustomer(txtName.getText(), txtAddress.getText(), txtPhone.getText());
                JOptionPane.showMessageDialog(null, "Customer account created successfully for " + c.getName());
                populateTable();
                btnNewCustomer.setEnabled(true);
                txtUsername.setText("");
                txtUsername.setEnabled(false);
                txtPassword.setText("");
                txtPassword.setEnabled(false);
                txtRePassword.setText("");
                txtRePassword.setEnabled(false);
                txtName.setText("");
                txtName.setEnabled(false);
                txtPhone.setText("");
                txtPhone.setEnabled(false);
                txtAddress.setText("");
                txtAddress.setEnabled(false);
                btnSubmit.setEnabled(false);
            }
            else
            {
                ecosystem.getEmployeeDirectory().deleteEmployee(e);
                JOptionPane.showMessageDialog(null,"Username " + txtUsername.getText() + " already exists!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        else
        {
            return;
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

