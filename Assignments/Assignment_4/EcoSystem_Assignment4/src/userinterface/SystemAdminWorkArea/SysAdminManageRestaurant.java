    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        Restaurant r = ecosystem.getRestaurantDirectory().createRestaurant(txtName.getText(), txtAddress.getText());
        if(r == null)
        {
            JOptionPane.showMessageDialog(null,"Restaurant " + txtName.getText() + " already exists!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Restaurant created successfully as " + r.getName());
            btnNewRestaurant.setEnabled(true);
            txtName.setEnabled(false);
            txtAddress.setEnabled(false);
            btnSubmit.setEnabled(false);  
            txtName.setText("");
            txtAddress.setText("");
        }
        populateTable();
    }//GEN-LAST:event_btnSubmitActionPerformed

