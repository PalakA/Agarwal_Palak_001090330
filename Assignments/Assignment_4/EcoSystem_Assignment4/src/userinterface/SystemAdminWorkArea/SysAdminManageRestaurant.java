    public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)tblRestaurants.getModel();
        dtm.setRowCount(0);
        int count1 = 1;
        if(ecosystem.getRestaurantDirectory().getRestaurantList() != null)
        {
            for(Restaurant r : ecosystem.getRestaurantDirectory().getRestaurantList())
            {
                Object[] row = new Object[dtm.getColumnCount()];
                row[0] = r.getId();
                row[1] = r;
                row[2] = r.getAddress();
                dtm.addRow(row);
                count1++;
            }
        }
        else
        {
            tblRestaurants.setEnabled(false);
        }
    }

