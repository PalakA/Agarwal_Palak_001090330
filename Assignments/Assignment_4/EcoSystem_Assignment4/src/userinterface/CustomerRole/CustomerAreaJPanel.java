    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if((txtAddress.getText() == "") || (txtPhone.getText() == ""))
        {
            JOptionPane.showMessageDialog(null,"Please fill all values!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else 
        {
            customer.setAddress(txtAddress.getText());
            customer.setPhone(txtPhone.getText());
        }       
        valueLabel.setText(rest.getName());
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void jComboRestaurantsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboRestaurantsActionPerformed
        // TODO add your handling code here:
        if(jComboRestaurants.getSelectedItem() != "")
        {
            String res = jComboRestaurants.getSelectedItem().toString();
            rest = system.getRestaurantDirectory().findRestaurant(res);
            CustomerPlaceOrder cpo = new CustomerPlaceOrder(userProcessContainer, customer, rest);
            userProcessContainer.add("CustomerOrder", cpo);
            CardLayout layout = (CardLayout)this.userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_jComboRestaurantsActionPerformed

