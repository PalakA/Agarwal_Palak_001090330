    public CustomerAreaJPanel(JPanel userProcessContainer, UserAccount user, EcoSystem system) {
        initComponents();
        this.userProcessContainer = userProcessContainer;
        this.user = user;
        this.system = system;
        //System.out.println(system.getCustomerDirectory().getCustomerList().size());
        for(Customer c : system.getCustomerDirectory().getCustomerList() )
        {
            
                if(c.getName() == null ? user.getEmployee().getName() == null : c.getName().equals(user.getEmployee().getName()))
                {
                    this.customer = c;
                }
           
        }
        
        //System.out.println(this.customer.getName());
        txtName.setText(this.customer.getName());
        txtAddress.setText(this.customer.getAddress());
        txtPhone.setText(this.customer.getPhone());
        valueLabel.setText(this.customer.getName());
        jComboRestaurants.addItem("");
        for(Restaurant r : system.getRestaurantDirectory().getRestaurantList())
        {
            jComboRestaurants.addItem(r.getName());
        }
    }
    
