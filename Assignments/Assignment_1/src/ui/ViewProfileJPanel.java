    private void displayProfileInfo() {
        //Basic Information
        txtFirstName.setText(basicInfo.getFirstName());
        txtLastName.setText(basicInfo.getLastName());
        txtSSN.setText(basicInfo.getSsn());
        txtDob.setText(basicInfo.getDateOfBirth());
        txtPhoneNum.setText(basicInfo.getPhoneNumber());
        txtAge.setText(basicInfo.getAge());
        txtWeight.setText(basicInfo.getWeight());
        txtHeight.setText(basicInfo.getHeight());
        
        //Address Information
        txtStreetAdd.setText(addInfo.getStreetAddress());
        txtCity.setText(addInfo.getCity());
        txtState.setText(addInfo.getState());
        txtZipCode.setText(addInfo.getZipCode());
        
        //Saving Account Information
        txtSavingBankName.setText(savingAccInfo.getBankName());
        txtSavingRoutingNum.setText(savingAccInfo.getBankRoutingNum());
        txtSavingAccNum.setText(savingAccInfo.getBankAccNum());
        txtSavingAccBalance.setText(savingAccInfo.getAccBalance());
        
        //Checking Account Information
        txtCheckingBankName.setText(checkingAccInfo.getBankName());
        txtCheckingRoutingNum.setText(checkingAccInfo.getBankRoutingNum());
        txtCheckingAccNum.setText(checkingAccInfo.getBankAccNum());
        txtCheckingAccBalance.setText(checkingAccInfo.getAccBalance());
        
        //DL Information
        txtLicenseNum.setText(dlInfo.getLicenseNum());
        txtIssueDate.setText(dlInfo.getIssueDate());
        txtExpDate.setText(dlInfo.getExpDate());
        txtBloodType.setText(dlInfo.getBloodType());
        dlImage = dlInfo.getDlImage();
        if(dlImage !=null){
        imageLabelView.setIcon(dlImage.getIcon());
        }
        
        //Medical Information
        txtRecordNum.setText(medicalRecords.getMedicalRecordNum());
        txtAlergy1.setText(medicalRecords.getAlergy1());
        txtAlergy2.setText(medicalRecords.getAlergy2());
        txtAlergy3.setText(medicalRecords.getAlergy3());
    }


